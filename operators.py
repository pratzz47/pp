#Arithmetic Operators
x= 4	
y= 2
print("x : ", x)
print("y : ", y)
print("x + y : ",x + y)
print("x - y : ",x - y)
print("x * y : ",x * y)
print("x / y : ",x / y)
print("x ** y : ",x ** y)
print("x // y : ",x // y)
print("x % y : " ,x % y)

#Logical Operators
a = True
b = False
print('a and b is',a and b)
print('a or b is',a or b)
print('not a is',not a)

#Bitwise Operator
i = 4
j = 2
print("i : ", i)
print("j : ", j)
print("i & j : ", i&j)
print("i | j : ", i|j)
print("i ^ j : ", i^j)
print("i << j : ", i<<j)
print("i >> j : ", i>>j)


